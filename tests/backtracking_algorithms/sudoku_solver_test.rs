use pofk_algorithm::backtracking_algorithms::sudoku_solver::solve_sudoku;

#[test]
fn test_sudoku_solver() {
    let mut board = [
        ['5','3','.','.','7','.','.','.','.'],
        ['6','.','.','1','9','5','.','.','.'],
        ['.','9','8','.','.','.','.','6','.'],
        ['8','.','.','.','6','.','.','.','3'],
        ['4','.','.','8','.','3','.','.','1'],
        ['7','.','.','.','2','.','.','.','6'],
        ['.','6','.','.','.','.','2','8','.'],
        ['.','.','.','4','1','9','.','.','5'],
        ['.','.','.','.','8','.','.','7','9']
    ];
    assert!(solve_sudoku(&mut board));
    assert_eq!(board[0][0], '5');
}
